    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
       
        
        
        int red = tblRacuni.getSelectedRow();
        ModelTabeleRacuna mtr = (ModelTabeleRacuna) tblRacuni.getModel();
        
        if(red != -1){
            Racun r= mtr.getRacun(red);
            System.out.println(r.getRacunID());
            
             jPanel1.setVisible(true);
            try {
            
            StavkaRacuna sr = new StavkaRacuna();
            sr.setRacun(r);
            
            List<StavkaRacuna> listaStavki = Controller.getInstance().findBillItems(sr);
            ModelTabeleStavke mts = new ModelTabeleStavke(listaStavki);
            
            
            tblStavke.setModel(mts);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Niste popunili tabelu");
            ex.printStackTrace();
        }
            
            
        }
        
        
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        // TODO add your handling code here:
        
        int red = tblRacuni.getSelectedRow();
        ModelTabeleRacuna mtr = (ModelTabeleRacuna) tblRacuni.getModel();
        
        if (red == -1) {
            JOptionPane.showMessageDialog(this, "Sistem ne može da učita podatke o odabranom računu", "Greška", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        
        
       
          Racun r = mtr.getRacun(red);
          
          if(r.getStatusRacuna().equals(StatusRacuna.PLACEN_KARTICOM) || r.getStatusRacuna().equals(StatusRacuna.PLACEN_KESOM)){
            JOptionPane.showMessageDialog(this, "Sistem ne može da izmeni već plaćene račune!", "Greška", JOptionPane.ERROR_MESSAGE);
            return;
          }
          
          new FrmKreiranjeRacuna(r).setVisible(true);
            
            
        
        
        
    }//GEN-LAST:event_btnIzmeniActionPerformed

    private void btnPlatiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPlatiActionPerformed
        // TODO add your handling code here:
        int red = tblRacuni.getSelectedRow();
        ModelTabeleRacuna mtr = (ModelTabeleRacuna) tblRacuni.getModel();
        
        if(red != -1){
            Racun r = mtr.getRacun(red);
            
           if (r.getStatusRacuna().equals(StatusRacuna.PLACEN_KARTICOM) || r.getStatusRacuna().equals(StatusRacuna.PLACEN_KESOM)) {
                JOptionPane.showMessageDialog(this, "Račun je već plaćen!", "Greška", JOptionPane.ERROR_MESSAGE);
                return;
}
            new FrmPlatiRacun(r).setVisible(true);
            
            
        }
        
    }//GEN-LAST:event_btnPlatiActionPerformed

