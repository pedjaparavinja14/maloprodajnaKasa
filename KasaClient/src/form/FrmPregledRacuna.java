    private void popuniTabeluRacuna() {
        try {
            List<Racun> listaRacuna = Controller.getInstance().getAllBills(kasir);
            ModelTabeleRacuna mtr = new ModelTabeleRacuna(listaRacuna);
            tblRacuni.setModel(mtr);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Niste popunili tabelu");
            ex.printStackTrace();
        }
        
    }

    
